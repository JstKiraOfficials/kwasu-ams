/** NUC minimum attendance threshold — below this percentage a student is exam-barred. */
export const ATTENDANCE_THRESHOLD_NUC: number = 75;

/** Safe zone — at or above this percentage the student is in the green. */
export const ATTENDANCE_THRESHOLD_SAFE: number = 80;

/** Lower warning boundary — between this and SAFE the student is in the amber zone. */
export const ATTENDANCE_THRESHOLD_WARNING_LOW: number = 77;

/** Minimum percentage for conditional eligibility (with approved medical excuse). */
export const ATTENDANCE_THRESHOLD_CONDITIONAL: number = 70;
